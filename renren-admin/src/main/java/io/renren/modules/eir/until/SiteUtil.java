package io.renren.modules.eir.until;

public class SiteUtil {

    public static String transition(String siteType){
        String site = "";
        switch (siteType) {
            case "D":
                site = "内贸";
                break;
            case "I":
                site = "外贸";
                break;
        }
        return site;
    }

}
