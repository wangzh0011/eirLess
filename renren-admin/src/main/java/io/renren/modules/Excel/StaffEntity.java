package io.renren.modules.Excel;

import cn.afterturn.easypoi.excel.annotation.Excel;

/**
 * Created by timmy.deng on 2018/12/4.
 */
public class StaffEntity {
    @Excel(name = "no")
    private String no;
    @Excel(name = "name")
    private String name;
    @Excel(name = "department")
    private String department;
    @Excel(name = "department_chn")
    private String department_chn;
    @Excel(name = "position")
    private String position;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getDepartment_chn() {
        return department_chn;
    }

    public void setDepartment_chn(String department_chn) {
        this.department_chn = department_chn;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }
}
