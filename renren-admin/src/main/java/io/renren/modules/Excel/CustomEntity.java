package io.renren.modules.Excel;

import cn.afterturn.easypoi.excel.annotation.Excel;

/**
 * Created by timmy.deng on 2018/11/30.
 */
public class CustomEntity {

    @Excel(name = "ECN4_USER_ID")
    private String ECN4_USER_ID;
    @Excel(name = "ECN4_USER_NAME")
    private String ECN4_USER_NAME;
    @Excel(name = "FACILITY_ID")

    private String FACILITY_ID;

    @Excel(name = "department")
    private String department;

    public String getDepartment() {
        return department;
    }

    public void setDepartment(String department) {
        this.department = department;
    }

    public String getECN4_USER_ID() {
        return ECN4_USER_ID;
    }

    public void setECN4_USER_ID(String ECN4_USER_ID) {
        this.ECN4_USER_ID = ECN4_USER_ID;
    }

    public String getECN4_USER_NAME() {
        return ECN4_USER_NAME;
    }

    public void setECN4_USER_NAME(String ECN4_USER_NAME) {
        this.ECN4_USER_NAME = ECN4_USER_NAME;
    }

    public String getFACILITY_ID() {
        return FACILITY_ID;
    }

    public void setFACILITY_ID(String FACILITY_ID) {
        this.FACILITY_ID = FACILITY_ID;
    }
}
