package io.renren.modules.miniapp.entity.model;


import io.renren.modules.miniapp.entity.vo.OrderVo;

import java.util.List;

/**
 * Created by timmy.deng on 2018/8/30.
 */
public class OrderModel {
    private String time;//预约到港时间
    private List<OrderVo> list;
    private String  activityQuantity; //作业数量，几交几提
    private boolean isAdd;//是否可以添加新的业务

    public boolean isAdd() {
        return isAdd;
    }

    public void setAdd(boolean add) {
        isAdd = add;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public List<OrderVo> getList() {
        return list;
    }

    public void setList(List<OrderVo> list) {
        this.list = list;
    }

    public String getActivityQuantity() {
        return activityQuantity;
    }

    public void setActivityQuantity(String activityQuantity) {
        this.activityQuantity = activityQuantity;
    }
}
